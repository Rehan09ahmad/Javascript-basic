// Assignment Operator
{
    let a = 5;
    let b = 2;
    
    a += 4;   //  5 + 4 = 9 
    console.log(a) // answer 9
}

{
    let a = 5;
    let b = 2;

    a -= 4;  // 5 - 4 = 1
    console.log( a) // answer 1
}

{
    let a = 5;
    let b = 2;

    a *= 4; //  5 * 4 = 20 
    console.log(a) // answer 20   
}

{
    let a = 5;
    let b = 2;

    a %= 4;  // 5 % 4 = 1
    console.log( a) // answer 1
}

{
    let a = 5;
    let b = 2;

    a **= 4; // 5 * 5 * 5 * 5 = 625
    console.log(a) // answer 625
}